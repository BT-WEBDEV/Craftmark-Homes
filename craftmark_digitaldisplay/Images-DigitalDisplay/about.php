  <h1>The Best Keeps Getting Better</h1>

<p>Since 1991 the Craftmark Group, comprised of Craftmark Homes, Craftstar Homes, and BeaconCrest Homes, has successfully built and sold over 6,000 homes in the Washington, DC metropolitan area. </p>
<p>Over the years, the Craftmark family of new home builders has established meticulous detailing and distinctive stone, brick and siding architecture as hallmarks of their homes. At the same time, new benchmarks have been set in conscientious home construction, healthier home environments, and energy efficiency for monthly utility savings. </p>
